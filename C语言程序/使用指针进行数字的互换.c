#include <stdio.h>



void swap (int *p,int *q){
	int t;
	
	t = *p;
	*p = *q;
	*q = t;

}

int main (){
	int a;
	int b;


	printf ("请输入两个数字：");
	scanf ("%d %d",&a,&b);
	
	swap (&a,&b);
	
	printf ("现在a和b的值是：%d %d",a,b);
}

