#include <stdio.h>
int main (){
	int i,j;
	int a[3][3] = {
	{1,2,3},
	{1,2,3},
	{1,2,3}
	};
	
	for (i = 0;i<3;i++){
		for (j = 0;j<3;j++){
			printf ("%d",a[i][j]);
		}
		printf ("\n");
	}
	
}

/*
在命令行中的输出结果为：
-----------------------------
123
123
123
-----------------------------
*/