#include <stdio.h>
int main (){
	int i,j;
	scanf ("%d %d",&i,&j);
	if (i>j){                         //如果i>j,输出下面语句
		printf ("i大于j\n");          //输出i大于j
	}else{                            //否则，换句话说就是i<=j
		printf ("i小于j\n");          //输出i小于j
	}
}
/*
在命令行中的输出结果为：
--------------------------------
1 2
i小于j

2 1
i大于j

注意：该方案的else中包含了i<=j的情况，即输入两个一样的数字也输出i小于j
--------------------------------
*/